/**
 * A, B, C = Rock, Paper, Scissors
 * X, Y, Z = Rock, Paper, Scissors
 * B beats X,
 * A beats C
 * C beats B
 */

type Move = "R" | "P" | "C";
type End = "lost" | "won" | "draw";

const scoring = {
  inp: {
    R: 1,
    P: 2,
    C: 3,
  },
  end: {
    lost: 0,
    draw: 3,
    won: 6,
  },
};

const xyzRPC = {
  X: "R",
  Y: "P",
  Z: "C",
};

// Score of a single round
const scoreInit = (str: string) => {
  const [a, b] = str.split(" ");
  let end: End;

  if (a === "A") {
    if (b === "X") {
      end = "draw";
    } else if (b === "Y") {
      end = "won";
    } else if (b === "Z") {
      end = "lost";
    }
  } else if (a === "B") {
    if (b === "X") {
      end = "lost";
    } else if (b === "Y") {
      end = "draw";
    } else if (b === "Z") {
      end = "won";
    }
  } else if (a === "C") {
    if (b === "X") {
      end = "won";
    } else if (b === "Y") {
      end = "lost";
    } else if (b === "Z") {
      end = "draw";
    }
  }

  const s = scoring.inp[xyzRPC[b]] + scoring.end[end];

  console.log(`${str} ${s}`);

  return s;
};

const properScoring = {
  // X means you need to lose, Y means you need to end the round in a draw, and Z means you need to win.
  end: {
    X: "lost",
    Y: "draw",
    Z: "won",
  },
} as const;

const scoreProer = (str: string) => {
  const [a, b] = str.split(" ");
  const end: End = properScoring.end[b] as End;
  let endMove: Move;

  switch (a) {
    case "A": {
      if (end === "won") endMove = "P";
      else if (end === "draw") endMove = "R";
      else if (end === "lost") endMove = "C";
      break;
    }
    case "B": {
      if (end === "won") endMove = "C";
      else if (end === "draw") endMove = "P";
      else if (end === "lost") endMove = "R";
      break;
    }
    case "C": {
      if (end === "won") endMove = "R";
      else if (end === "draw") endMove = "C";
      else if (end === "lost") endMove = "P";
      break;
    }
  }

  const score = scoring.end[end] + scoring.inp[endMove];
  console.log({ str }, { end, endMove, score });
  return score;
};

